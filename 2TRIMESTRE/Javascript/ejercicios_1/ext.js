function ocultar() {
    document.getElementById("rojo").style.opacity= "0.0";
    document.getElementById("verde").style.opacity= "0.0";
    document.getElementById("azul").style.opacity= "0.0";
}

function mostrar() {
    document.getElementById("rojo").style.opacity= "1";
    document.getElementById("verde").style.opacity= "1";
    document.getElementById("azul").style.opacity= "1";
}